    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String username = userText.getText();
        String password = passText.getText();
        if (d.saveData(username.trim(), password.trim())) {
            mainPanel.removeAll();
            mainPanel.add(addPassengerPanel);
            mainPanel.repaint();
            mainPanel.revalidate();

            try {
                myCon = DriverManager.getConnection(connectionURL, d.getUser(), d.getPass());
                Statement tempStatement = myCon.createStatement();
                ResultSet temp = tempStatement.executeQuery("SELECT name FROM countries");
                while (temp.next()) {
                    depBox.addItem(temp.getString("name"));
                }
                temp.beforeFirst();
                while (temp.next()) {
                    arrBox.addItem(temp.getString("name"));
                }
            } catch (SQLException ex) {
                Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            logFail.setVisible(true);
        }
    }//GEN-LAST:event_loginButtonActionPerformed

