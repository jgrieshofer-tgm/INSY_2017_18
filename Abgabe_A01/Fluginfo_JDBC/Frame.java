    private void arrBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_arrBoxActionPerformed
        try {
            arrBox2.removeAllItems();
            String arrCountry = (String) arrBox.getSelectedItem();
            myCon = DriverManager.getConnection(connectionURL, d.getUser(), d.getPass());
            Statement tempStatement = myCon.createStatement();
            ResultSet temp = tempStatement.executeQuery("SELECT code From countries WHERE name = \"" + arrCountry + "\";");
            temp.next();
            String arrCode = temp.getString("code");
            temp = tempStatement.executeQuery("SELECT name From airports WHERE country = \"" + arrCode + "\" ORDER BY name;");
            while (temp.next()) {
                arrBox2.addItem(temp.getString("name"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_arrBoxActionPerformed

